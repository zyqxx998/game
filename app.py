def render_progress():
    total = len(st.session_state.levels)
    done  = st.session_state.current
    st.progress(done / total, text=f"第 {done}/{total} 关  |  当前守护值：{st.session_state.score} 分")


def render_scene(cg_id: str):
    scene = SCENES[cg_id]
    st.markdown(f"### 📍 {scene['title']}")
    with st.container(border=True):
        st.markdown(f"*{scene['description']}*")
        st.markdown(f"> 💬 **{scene['npc_say']}**")

    st.markdown("#### 你的应对方式：")
    col_a, col_b, col_c = st.columns(3)
    with col_a:
        st.button(f"🅐 {scene['options']['A']}", key="btn_A", on_click=choose, args=("A",), use_container_width=True)
    with col_b:
        st.button(f"🅑 {scene['options']['B']}", key="btn_B", on_click=choose, args=("B",), use_container_width=True)
    with col_c:
        st.button(f"🅒 {scene['options']['C']}", key="btn_C", on_click=choose, args=("C",), use_container_width=True)


def render_result():
    option, result_text, delta = st.session_state.last_choice
    color = "#27ae60" if delta > 0 else ("#e74c3c" if delta < 0 else "#8e44ad")
    sign  = "+" if delta > 0 else ""
    label = "✅ 做得很好！" if delta > 0 else ("⚠️ 有待改进" if delta < 0 else "📌 中性处理")

    st.markdown(f"**你选择了：** {option}")
    with st.container(border=True):
        st.markdown(result_text)
        st.markdown(
            f'<span style="color:{color};font-weight:bold;font-size:1.2rem;">{label}  {sign}{delta} 分</span>',
            unsafe_allow_html=True,
        )

    # 特殊成就提示
    last_cg = st.session_state.levels[st.session_state.current]
    scene = SCENES[last_cg]
    if "special_achievement" in scene and option in scene["special_achievement"]:
        ach = scene["special_achievement"][option]
        st.success(f"🏅 解锁成就：**{ach}**")

    is_last = st.session_state.current >= len(st.session_state.levels) - 1
    if is_last:
        st.button("📊 查看最终报告", on_click=next_scene, use_container_width=True, type="primary")
    else:
        st.button("➡️ 下一关", on_click=next_scene, use_container_width=True, type="primary")


def render_final():
    score = st.session_state.score
    title, poem, emoji = get_rank(score)

    st.markdown(
        f"""
        <div style="background:linear-gradient(135deg,#ffe4ec,#fff0f5);border-radius:16px;
                    padding:32px;text-align:center;margin-bottom:24px;
                    box-shadow:0 4px 20px rgba(255,105,135,.2);">
            <div style="font-size:4rem;">{emoji}</div>
            <h2 style="color:#c0004e;margin:8px 0;">{title}</h2>
            <p style="color:#888;font-style:italic;">{poem}</p>
            <div style="font-size:2rem;font-weight:bold;color:#c0004e;margin-top:12px;">
                最终守护值：{score} 分
            </div>
        </div>
        """,
        unsafe_allow_html=True,
    )

    if st.session_state.achievements:
        st.markdown("### 🏅 特殊成就")
        for ach in st.session_state.achievements:
            st.success(f"🌸 **{ach}**")

    st.markdown("### 📋 本局回顾")
    for i, (cg_id, choice, delta, result_text) in enumerate(st.session_state.history, 1):
        sign  = "+" if delta > 0 else ""
        color = "green" if delta > 0 else ("red" if delta < 0 else "gray")
        with st.expander(f"第{i}关 · {SCENES[cg_id]['title']}  [{sign}{delta}分]"):
            st.markdown(f"**选项 {choice}**：{result_text}")

    st.button("🔄 再来一局", on_click=start_game, use_container_width=True, type="primary")


