def main():
    st.set_page_config(
        page_title="樱花守护者 · 武汉大学",
        page_icon="🌸",
        layout="centered",
    )
    init_state()
    render_header()

    st.markdown(
        '<div class="cherry-header"><h1>🌸 樱花守护者</h1>'
        '<p>武汉大学志愿服务模拟系统</p></div>',
        unsafe_allow_html=True,
    )

    if not st.session_state.started:
        st.markdown(
            """
            欢迎来到**武汉大学樱花守护者**！

            你将化身一名红马甲志愿者，在樱花季高峰时段守护珞珈山的樱花大道。
            面对15个随机场景，做出最佳抉择，积累守护值，解锁成就，最终获得你的专属评价。

            - 🎯 每局从 30 个场景中随机抽取 **15 关**
            - ⭐ 特殊场景 4 关 · 中性场景 1 关 · 普通场景 10 关
            - 📊 满分约 100 分，共 6 个等级称号
            """,
            unsafe_allow_html=True,
        )
        st.button("🌸 开始守护任务", on_click=start_game, use_container_width=True, type="primary")
        return

    idx = st.session_state.current
    total = len(st.session_state.levels)

    # 游戏结束
    if idx >= total:
        render_final()
        return

    render_progress()
    cg_id = st.session_state.levels[idx]

    if st.session_state.phase == "question":
        render_scene(cg_id)
    else:
        render_result()


if __name__ == "__main__":
    main()