# 2. 关卡抽取逻辑  LEVEL SELECTION
# ─────────────────────────────────────────────

SPECIAL_POOL = ["CG17", "CG25", "CG28", "CG29", "CG30"]
NEUTRAL_POOL = ["CG18", "CG24", "CG26"]
COMMON_POOL = [
    "CG1", "CG2", "CG3", "CG4", "CG5", "CG6", "CG7", "CG8",
    "CG9", "CG10", "CG11", "CG12", "CG13", "CG14", "CG15", "CG16",
    "CG19", "CG20", "CG21", "CG22", "CG23", "CG27",
]


def draw_levels() -> list[str]:
    """抽取15关：特殊4 + 中性1 + 普通10，全随机打乱"""
    special = random.sample(SPECIAL_POOL, 4)
    neutral = random.sample(NEUTRAL_POOL, 1)
    common  = random.sample(COMMON_POOL, 10)
    pool = special + neutral + common
    random.shuffle(pool)
    return pool


