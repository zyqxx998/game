def init_state():
    if "started" not in st.session_state:
        st.session_state.started = False
    if "levels" not in st.session_state:
        st.session_state.levels = []
    if "current" not in st.session_state:
        st.session_state.current = 0
    if "score" not in st.session_state:
        st.session_state.score = 0
    if "history" not in st.session_state:
        st.session_state.history = []   # [(cg_id, choice, delta)]
    if "achievements" not in st.session_state:
        st.session_state.achievements = []
    if "phase" not in st.session_state:
        st.session_state.phase = "question"  # "question" | "result"
    if "last_choice" not in st.session_state:
        st.session_state.last_choice = None


def start_game():
    st.session_state.levels = draw_levels()
    st.session_state.current = 0
    st.session_state.score = 0
    st.session_state.history = []
    st.session_state.achievements = []
    st.session_state.phase = "question"
    st.session_state.last_choice = None
    st.session_state.started = True


def choose(option: str):
    idx = st.session_state.current
    cg_id = st.session_state.levels[idx]
    scene = SCENES[cg_id]
    result_text, delta = scene["results"][option]
    st.session_state.score += delta
    st.session_state.history.append((cg_id, option, delta, result_text))
    # 特殊成就
    if "special_achievement" in scene and option in scene["special_achievement"]:
        ach = scene["special_achievement"][option]
        if ach not in st.session_state.achievements:
            st.session_state.achievements.append(ach)
    st.session_state.last_choice = (option, result_text, delta)
    st.session_state.phase = "result"


def next_scene():
    st.session_state.current += 1
    st.session_state.phase = "question"
    st.session_state.last_choice = None


